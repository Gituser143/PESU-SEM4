;Display 0-F and F-0 on the 8 segment display.

.text
BEGIN: MOV R0,#0
MOV R2,#0
AGAIN: SWI 0X202 ;CHECK IF THE BLACK BUTTON'S PRESSED OR NOT
CMP R0,#1
BEQ LOOP1
CMP R0,#2
BEQ LOOP2
B AGAIN

LOOP1: MOV R5,#16
LDR R1,=ZERO
BACK1: LDRB R0,[R1]
SWI 0X200 ; SET 8 SEG DISPLAY TO LIGHT UP
BL DELAY
ADD R1,R1,#1
SUB R5,R5,#1
CMP R5,#0
BNE BACK1
B AGAIN

LOOP2: MOV R5,#16
LDR R1,=F
BACK2: LDRB R0,[R1]
SWI 0X200
BL DELAY
SUB R1,R1,#1
SUB R5,R5,#1
CMP R5,#0
BNE BACK2
B AGAIN

DELAY: MOV R4,#16000
LOOP3: SUB R4,R4,#1
CMP R4,#0
BGE LOOP3
MOV PC,LR

.data
